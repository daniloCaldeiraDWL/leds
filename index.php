                  <p>

                  O DengueME (Dengue Modeling Environment) é um framework de software projetado para auxiliar na modelagem e simulação da dinâmica populacional do mosquito Aedes aegypti e de transmissão da Dengue. Ele oferece aos usuários uma biblioteca de modelos (entomológicos e epidemiológicos) que podem ser utilizados na construção de cenários e simulações visando apoiar o processo de tomada de decisão em políticas públicas para intervenção e controle da Dengue. Também pode ser utilizado como ferramenta de apoio ao ensino e pesquisa. O framework é extensível e permite adicionar novos modelos à sua biblioteca. Dentre os principais benefícios oferecidos pela plataforma DengueME temos a criação de modelos e cenários, simulação e visualização de resultados a partir de uma interface gráfica. Os modelos e o framework DengueME tem como base a plataforma de modelagem e simulação ambiental TerraME (Terra Modeling Environment). O framework também irá suportar modelos desenvolvidos na linguagem R.
                  <br>
                  O TerraME (www.terrame.org) é uma plataforma de modelagem e simulação da dinâmica espaço-temporal de fenômenos ambientais. Ela oferece suporte  para a criação de modelos sobre espaços celulares bi-dimensionais baseados em autômatos celulares, agentes, e em rede. O TerraME oferece uma interface para a biblioteca TerraLib, permitindo a integração com bases de dados geográficos. Através da linguagem de modelagem TerraML, ela permite desenvolver modelos multi-escala e multi-paradigmas para aplicações ambientais.
                  <br>
                  O DengueME está sendo desenvolvido e distribuído como uma aplicação desktop. O DengueME se encontra em fase de desenvolvimento, atualmente na versão alpha, e tem como base os seguintes softwares: TerraME (versão 1.2.2.20130604), Qt (versão 4.8.1), QtCreator (versão 2.7.0).
